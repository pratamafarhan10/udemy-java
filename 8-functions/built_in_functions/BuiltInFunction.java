public class BuiltInFunction {
    public static void main(String[] args) {
        System.out.println("Hello world!");

        double sine = Math.sin(1.2);

        System.out.println("The sin of 1.2 is " + sine);

        double power = Math.pow(2, 4);

        System.out.println("2 ^ 4 is " + power);

        double ceiling = Math.ceil(3.6);

        System.out.println("Ceiling 3.6 is " + ceiling);

        double floor = Math.floor(3.6);

        System.out.println("Floor 3.6 is " + floor);
    }    
}
